# Apresentar em ordem alfabética as disciplinas existentes nesta escola.
# O objetivo desta consulta abaixo é ordenar todas as disciplinas que antes estavam em ordem crescente em relaçao ao 'idDisciplinas' para agora na ordem alfabéticas para melhor visualização.
# Observe que para fins de consultas ou sorteio, esta ordem de disciplinas é mais fácil de ser identificada.

select * from disciplinas
order by nomeDisciplina;
